install.packages("xlsx")
library("xlsx")
install.packages("arules")
library("arules")
dataset<-read.xlsx("groceries.xlsx",sheetIndex=1)
attach(dataset)
head(dataset)
rules<-apriori(dataset,parameter=list(supp=0.001,conf=0.8))
options(digits=2)
inspect(rules[1:5])
summary(rules)
rules<-sort(rules,decreasing=TRUE,by="confidence")
inspect(rules[1:5])
inspect(rules)
